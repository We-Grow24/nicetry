/**
 * /api/export-game
 *
 * Accepts: POST { project_id: string }
 *   OR:    GET  ?project_id=<uuid>
 *
 * Fetches the project's master_director_seed from Supabase, then builds a ZIP
 * containing:
 *   godot_seed.json     — full director seed (world / characters / narrative)
 *   project.godot       — minimal Godot 4 project descriptor
 *   World.gd            — GDScript bootstrap that reads godot_seed.json at runtime
 *   export_presets.cfg  — template export preset for Windows / Linux / Web
 *   README.md           — step-by-step guide: open in Godot → tweak → export EXE
 *
 * The GDScript reads the seed file at runtime so designers can tweak values
 * without touching code — just edit godot_seed.json and re-run.
 */

import { NextRequest, NextResponse } from "next/server";
import { createClient } from "@/lib/supabase/server";
import JSZip from "jszip";

// ─── Types ────────────────────────────────────────────────────────────────────

interface CharacterSeed {
  id: string;
  role: string;
  appearance_seed: number;
  [key: string]: unknown;
}

interface WorldSeed {
  biome?: string;
  time_of_day?: number;
  weather?: string;
  [key: string]: unknown;
}

interface MasterDirectorSeed {
  project_name?: string;
  world?: WorldSeed;
  characters?: CharacterSeed[];
  narrative?: Record<string, unknown>;
  [key: string]: unknown;
}

// ─── GDScript bootstrap ───────────────────────────────────────────────────────

function buildWorldGd(name: string): string {
  return `extends Node

## World.gd — auto-generated by TailAdmin Game Export
## Reads godot_seed.json at startup and applies it to the scene.
##
## HOW TO EXTEND:
##   1. Open the Godot editor.
##   2. Attach this script to your root scene node (e.g. "World").
##   3. Edit godot_seed.json to change biome, characters, and narrative parameters.
##   4. The seed is re-read every time the game launches (hot-reload in editor).

const SEED_PATH := "res://godot_seed.json"

var seed_data: Dictionary = {}

func _ready() -> void:
\tvar file := FileAccess.open(SEED_PATH, FileAccess.READ)
\tif file:
\t\tvar json := JSON.new()
\t\tjson.parse(file.get_as_text())
\t\tseed_data = json.get_data()
\t\tfile.close()
\t\t_apply_seed()
\t\tprint("[${name}] Seed loaded: ", seed_data.get("project_name", "unnamed"))
\telse:
\t\tpush_error("[${name}] Could not open godot_seed.json at: " + SEED_PATH)

func _apply_seed() -> void:
\tvar world: Dictionary = seed_data.get("world", {})
\tvar characters: Array  = seed_data.get("characters", [])
\tvar narrative: Dictionary = seed_data.get("narrative", {})

\t# ── World ────────────────────────────────────────────────────────────────
\t# TODO: swap out your Environment / WorldEnvironment based on world.biome
\tvar biome: String  = world.get("biome", "forest")
\tvar tod: float     = float(world.get("time_of_day", 12))
\tvar weather: String = world.get("weather", "clear")
\tprint("  biome=%s  time=%s  weather=%s" % [biome, tod, weather])

\t# ── Characters ───────────────────────────────────────────────────────────
\tfor char_data in characters:
\t\tvar char_id: String     = char_data.get("id", "unknown")
\t\tvar role: String        = char_data.get("role", "extra")
\t\tvar seed_val: float     = float(char_data.get("appearance_seed", 0.5))
\t\tprint("  char=%s  role=%s  seed=%.3f" % [char_id, role, seed_val])
\t\t# TODO: instantiate character scenes and set their appearance from seed_val

\t# ── Narrative ────────────────────────────────────────────────────────────
\tvar act: int       = int(narrative.get("act", 1))
\tvar tension: float = float(narrative.get("tension", 0.5))
\tprint("  act=%d  tension=%.2f" % [act, tension])
\t# TODO: trigger quest events / AI dialogue based on act + tension
`;
}

function buildProjectGodot(name: string): string {
  const safeId = name.toLowerCase().replace(/\s+/g, "_").replace(/[^a-z0-9_]/g, "");
  return `; Engine configuration file — Godot 4.x
; Exported by TailAdmin Game Studio

[application]
config/name="${name}"
config/version="1.0"
run/main_scene="res://World.tscn"

[editor]
run/main_run_args=""

[filesystem]
import/blender/enabled=false

[physics]
common/physics_engine="GodotPhysics3D"

[rendering]
renderer/rendering_method="forward_plus"
environment/defaults/default_clear_color=Color(0.1, 0.1, 0.1, 1)

[project_settings_override]
application/config/project_data_path="res://.godot"
application/config/project_name="${safeId}"
`;
}

