function buildReadme(name: string, seed: MasterDirectorSeed): string {
  const biome   = seed.world?.biome ?? "forest";
  const chars   = (seed.characters ?? []).map(c => `- **${c.id}** (${c.role})`).join("\n");
  return `# ${name} — Godot 4 Export

Auto-generated by **TailAdmin Game Studio**.

## 📁 Files

| File | Purpose |
|------|---------|
| \`godot_seed.json\` | Full director seed — edit to change world & characters |
| \`project.godot\` | Godot 4 project descriptor — open this in the editor |
| \`World.gd\` | GDScript bootstrap — reads the seed at runtime |
| \`export_presets.cfg\` | Pre-configured export targets (Windows / Linux / Web) |

## 🚀 Quick Start

### 1. Install Godot 4

Download from <https://godotengine.org/download/>

### 2. Open the project

\`\`\`bash
godot4 --editor project.godot
\`\`\`

Or launch the Godot editor → **Import** → select \`project.godot\`.

### 3. Create a root scene

1. **Scene → New Scene**
2. Add a **Node3D** root node (rename to "World")
3. Attach **World.gd** as its script
4. Save as **World.tscn**

### 4. Export as EXE (Windows)

1. **Project → Export…**
2. Select the **Windows Desktop** preset (already configured in \`export_presets.cfg\`)
3. Click **Export Project** → choose an output directory
4. Run \`export/${name}.exe\`

### 5. Export for Web (HTML5)

Use the **Web** preset — output goes to \`export/web/index.html\`.
Serve with any static host (Netlify, Vercel, itch.io).

## 🌍 Seed Summary

**World:** biome \`${biome}\`, time ${seed.world?.time_of_day ?? 12}h, weather \`${seed.world?.weather ?? "clear"}\`

**Characters:**
${chars || "- none defined"}

**Narrative:** act ${seed.narrative?.act ?? 1}, tension ${seed.narrative?.tension ?? 0.4}

Edit \`godot_seed.json\` and restart the game to apply changes without recompiling.
`;
}

// ─── Route handler ────────────────────────────────────────────────────────────

async function getProjectId(req: NextRequest): Promise<string | null> {
  if (req.method === "GET") return req.nextUrl.searchParams.get("project_id");
  try { return (await req.json())?.project_id ?? null; } catch { return null; }
}

export async function GET(req: NextRequest)  { return handler(req); }
export async function POST(req: NextRequest) { return handler(req); }

async function handler(req: NextRequest) {
  const projectId = await getProjectId(req);
  if (!projectId) {
    return NextResponse.json({ error: "project_id is required" }, { status: 400 });
  }

  const sb = await createClient();
  const { data: project, error } = await sb
    .from("projects")
    .select("id, type, master_director_seed")
    .eq("id", projectId)
    .single();

  if (error || !project) {
    return NextResponse.json({ error: "Project not found" }, { status: 404 });
  }

  if (project.type !== "game") {
    return NextResponse.json(
      { error: `Project type is "${project.type}", expected "game"` },
      { status: 400 }
    );
  }

  const seed = (project.master_director_seed ?? {}) as MasterDirectorSeed;
  const name = seed.project_name ?? "My Game";
  const slug = name.toLowerCase().replace(/\s+/g, "-").replace(/[^a-z0-9-]/g, "");

  const zip = new JSZip();

  zip.file("godot_seed.json",     JSON.stringify(seed, null, 2));
  zip.file("project.godot",       buildProjectGodot(name));
  zip.file("World.gd",            buildWorldGd(name));
  zip.file("export_presets.cfg",  buildExportPresets(name));
  zip.file("README.md",           buildReadme(name, seed));

  // Placeholder scene — users replace with real .tscn content
  zip.file(
    "World.tscn",
    `[gd_scene load_steps=1 format=3 uid="uid://world_placeholder"]

[node name="World" type="Node3D"]
script = ExtResource("res://World.gd")
`
  );

  const buffer = await zip.generateAsync({ type: "nodebuffer" });

  return new Response(new Uint8Array(buffer), {
    headers: {
      "Content-Type": "application/zip",
      "Content-Disposition": `attachment; filename="${slug}-godot.zip"`,
      "Content-Length": String(buffer.length),
    },
  });
}
